	/**
	 * 管理员登录
	 * 
	 * @param evt
	 */
	private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {
		jButton3.setIcon(new ImageIcon("images/btn1.jpg"));
		Tools t = new Tools();
		String username = textField1.getText();
		String pw = new String(this.jPasswordField1.getPassword());

		if (t.isEmpty(username) || t.isEmpty(pw)) {
			JOptionPane.showMessageDialog(null, "用户名或密码不能为空");
		} else {
			Connection conn = t.getConn();
			User user = t.login(conn, username, pw);
			// /**
			// * 这个user用来PersonInfoFrm界面里获取当前登录者的信息
			// */
			// PersonInfoFrm p = new PersonInfoFrm();
			// p.TransUser(user);

			if (user != null) {
				
				if (!isValidCodeRight()) {
					JOptionPane.showMessageDialog(null, "验证码错误！");
				}
				if (isValidCodeRight()) {
					JOptionPane.showMessageDialog(null, "登录成功！");
					dispose();
					main_Frm main = new main_Frm();
					// main.tansUser(user);
					main.setVisible(true);
				} else {
					JOptionPane.showMessageDialog(null, "登录失败！");
				}
			}

		}

	}

