	/**
	 * 
	 * 验证码的校验
	 * 
	 * 
	 * 
	 * @return
	 */

	public boolean isValidCodeRight() {

		if (jtf_code == null) {

			return false;

		}

		if (validcode == null) {

			return true;

		}

		if (validcode.getCode().equals(jtf_code.getText())) {

			return true;

		}

		return false;

	}

