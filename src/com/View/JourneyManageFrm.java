	/**
	 * 删除旅行社
	 * 
	 * @param evt
	 */
	private void btndaleteActionPerformed(java.awt.event.ActionEvent evt) {
		String Jn_name = this.txtJn_name.getText();
		Tools t = new Tools();
		Journey_db journey = new Journey_db();
		if (t.isEmpty(Jn_name)) {
			JOptionPane.showMessageDialog(null, "请选择你要删除的旅行社");
		} else {
			Connection con = t.getConn();
			int n = journey.deljourney(con, Jn_name);
			if (n > 0) {
				JOptionPane.showMessageDialog(null, "删除成功!");
				resetValue();
				filltable(new Journey());
			} else {
				JOptionPane.showMessageDialog(null, "删除失败！");
			}

		}
	}

	/**
	 * 修改事件处理
	 * 
	 * @param evt
	 */
	private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {
		String Jn_year = this.txtJn_year.getText();
		String Jn_grade = this.txtJn_grade.getText();
		String Jn_details = this.txtJn_details.getText();
		String Jn_name = this.txtJn_name.getText();
		String Jn_plan = this.txtJn_plan.getText();
		String Jn_money = this.txtmoney.getText();
		if (Jn_plan.equals("")) {
			JOptionPane.showMessageDialog(null, "该旅行社目前没有出行计划，不能要钱");
			Jn_money = "";
		}
		Journey journey = new Journey();
		journey.setJn_details(Jn_details);
		journey.setJn_grade(Jn_grade);
		journey.setJn_name(Jn_name);
		journey.setJn_year(Jn_year);
		journey.setJn_plan(Jn_plan);
		journey.setJn_money(Jn_money);
		Connection con = null;
		Journey_db journey_db = new Journey_db();
		Tools t = new Tools();
		if (t.isEmpty(Jn_name)) {
			JOptionPane.showMessageDialog(null, "请选择你要修改的旅行社");
		} else {
			try {
				con = t.getConn();
				int n = journey_db.updateJourney(con, journey);
				if (n > 0) {
					JOptionPane.showMessageDialog(null, "修改成功！");
					resetValue();
					filltable(new Journey());

				} else {
					JOptionPane.showMessageDialog(null, "修改失败！");
				}
			} catch (Exception e) {
				e.printStackTrace();
			} finally {
				t.closeConn(con);
			}
		}
	}

